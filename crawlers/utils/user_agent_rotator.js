/**
 * Get a random user agent from the list
 * @returns {string} Random user agent
 */
function getRandomUserAgent() {
  const randomIndex = Math.floor(Math.random() * USER_AGENTS.length);
  return USER_AGENTS[randomIndex];
}

module.exports = {
  getRandomUserAgent,
};
